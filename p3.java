
public class p3 {
    public static void main(String[] args) {
        int[][] arr = new int[3][4];
        
        // Example: Print the array
        for (int i = 0; i < arr.length; i++) {
            for (int j = 0; j < arr[i].length; j++) {
                System.out.print(arr[i][j] + " ");
            }
            System.out.println();
        }
    }
}